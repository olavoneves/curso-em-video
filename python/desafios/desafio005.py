# Desenvolva um programa para ler duas notas de um aluno e o resultado seja a media

aluno = input('Digite seu nome: ')
nota01 = int(input('Digite sua primeira nota: '))
nota02 = int(input('Digite sua segunda nota: '))
media = (nota01 + nota02) / 2

print(f'A media do aluno {aluno}, foi de {media}!')
