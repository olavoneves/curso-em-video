# criar um programa para ler quanto dinheiro a pessoa tem na carteira e ver quantos dolares a pessoa pode comprar

print('\nPrograma para você ver quantos doláres pode comprar com o dinheiro que tem na carteira! \n')

dinheiroNaCarteira = float(input('Digite a quantodade de dinheiro que você tem na carteira: '))
dolares = dinheiroNaCarteira / 5.69

print(f'Você pode comprar {dolares:.2f} doláres \nNo dia 17/03/2025')
